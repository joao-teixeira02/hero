public class Position {

    private int x;
    private int y;

    public Position(int xx, int yy) {

        x = xx;
        y = yy;

    }
    public int getX() {

        return x;

    }
    public int getY() {

        return y;

    }
    public void setX(int xx) {

        x = xx;

    }
    public void setY(int yy) {

        y = yy;

    }

}
